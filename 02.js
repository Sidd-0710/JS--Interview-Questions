// Print the Number from 1 to 5 to the console Using loop

console.log("Using A for loop ");

for (let i = 1; i <=5; i++) {
    console.log(i); 
}

console.log("Using A while loop ");

let i =1;
while (i <= 5) {
    console.log(i);
    i++;
}

console.log("Using A Do-While loop ");

let j=1;
do {
    console.log(j);
    j++;
} while (j<=5);